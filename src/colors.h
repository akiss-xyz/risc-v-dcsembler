#pragma once

#define RED     "\x1b[31m"
#define GREEN   "\x1b[32m"
#define YELLOW  "\x1b[33m"
#define BLUE    "\x1b[34m"
#define MAGENTA "\x1b[35m"
#define CYAN    "\x1b[36m"
#define RESET   "\x1b[0m"

#define REDC(s) RED s RESET
#define GREENC(s) GREEN s RESET
#define YELLOWC(s) YELLOW s RESET
#define BLUEC(s) BLUE s RESET
#define MAGENTAC(s) MAGENTA s RESET
#define CYANC(s) CYAN s RESET
